/*
    NaN signifie "Not A Number". 
    Lorsqu'une fonction ou une opération mathématique
    en JavaScript ne peut pas renvoyer un nombre spécifique,
    elle renvoie la valeur NaN à la place.

    C'est une propriété de l'objet global et une référence à Number.NaN
*/

NaN;                        // => NaN
typeof NaN;                 // => number
NaN == NaN;                 // => false
NaN === NaN;                // => false

